
package Model;

public class HW {
    private double h;
    private double w;

    public HW(double h, double w) {
        this.h = h;
        this.w = w;
    }

    public HW() {
    }
    
    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }
    
}
